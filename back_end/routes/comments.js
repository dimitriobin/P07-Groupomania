'use strict'
const router = require('express').Router();
const auth = require('../middlewares/auth')

const { createOneComment, readAllComments, readAllCommentsByUser, readAllCommentsByPost, readOneComment, updateOneComment, deleteOneComment } = require ('../controllers/comments');

/////////////////////////////////////////////
// CREATE ONE Comment
/////////////////////////////////////////////
router.post('/', auth, createOneComment);

/////////////////////////////////////////////
// READ ALL Comments
/////////////////////////////////////////////
router.get('/', auth, readAllComments);

/////////////////////////////////////////////
// READ ALL Comments by user
/////////////////////////////////////////////
router.get('/user/:user_id', auth, readAllCommentsByUser);

/////////////////////////////////////////////
// READ ALL Comments by post
/////////////////////////////////////////////
router.get('/post/:post_id', auth, readAllCommentsByPost);

/////////////////////////////////////////////
// READ ONE Comment
/////////////////////////////////////////////
router.get('/:id', auth, readOneComment);

/////////////////////////////////////////////
// UPDATE ONE Comment
/////////////////////////////////////////////
router.put('/:id', auth, updateOneComment);


/////////////////////////////////////////////
// DELETE ONE Comment
/////////////////////////////////////////////
router.delete('/:id', deleteOneComment);




module.exports = router;
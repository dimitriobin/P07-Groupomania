const router = require('express').Router();

const { createOneSubject, readAllSubjects, readOneSubject, updateOneSubject, deleteOneSubject } = require ('../controllers/subjects');

/////////////////////////////////////////////
// CREATE ONE Subject
/////////////////////////////////////////////
router.post('/', createOneSubject);

/////////////////////////////////////////////
// READ ALL Subjects
/////////////////////////////////////////////
router.get('/', readAllSubjects);


/////////////////////////////////////////////
// READ ONE Subject
/////////////////////////////////////////////
router.get('/:subject_id', readOneSubject);


/////////////////////////////////////////////
// UPDATE ONE Subject
/////////////////////////////////////////////
router.put('/:subject_id', updateOneSubject);


/////////////////////////////////////////////
// DELETE ONE Subject
/////////////////////////////////////////////
router.delete('/:subject_id', deleteOneSubject);




module.exports = router;